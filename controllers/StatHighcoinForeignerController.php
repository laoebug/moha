    public function actionSave($year)
    {
        $user = Yii::$app->user->identity;
        $controller_id = Yii::$app->controller->id;
        $acton_id = Yii::$app->controller->action->id;
        if ($user->role["name"] != Yii::$app->params['DEFAULT_ADMIN_ROLE']) {
            if (!AuthenticationService::isAccessibleAction($controller_id, $acton_id)) {
                MyHelper::response(HttpCode::UNAUTHORIZED, Yii::t('app', 'HTTP Error 401- You are not authorized to access this operaton due to invalid authentication') . " with ID:  " . $controller_id . "/ " . $acton_id);
                return;
            }
        }

        $year = PhiscalYear::findOne($year);
        if (!isset($year)) {
            MyHelper::response(HttpCode::NOT_FOUND, Yii::t('app', 'Incorrect Phiscal Year'));
            return;
        }

        if ($year->status != 'O') {
            MyHelper::response(HttpCode::METHOD_NOT_ALLOWED, Yii::t('app', 'The Year is not allow to input'));
            return;
        }

        $post = Yii::$app->request->post();
        if (!isset($post)) {
            MyHelper::response(HttpCode::BAD_REQUEST, Yii::t('app', 'Incorrect Request Mehotd'));
            return;
        }

        $transaction = Yii::$app->db->beginTransaction();
        try {
            $model = StatHighcoinForeigner::find()
                ->where(['phiscal_year_id' => $year->id])
                ->one();
            if (!isset($model)) {
                $model = new StatHighcoinForeigner();
                $model->phiscal_year_id = $year->id;
                $model->user_id = Yii::$app->user->id;
            }
            $model->last_update = date('Y-m-d H:i:s');
            if (!$model->save()) throw new Exception(json_encode($model->errors));

            $detail = StatHighcoinForeignerDetail::find()
                ->where(['stat_3create_id' => $model->id, 'province_id' => $post['Stat3createDetail']['province']['id']])
                ->one();
            if (!isset($detail)) {
                $detail = new StatHighcoinForeignerDetail();
                $detail->ministry_id = $post['model']['ministry']['id'];
                $detail->stat_highcoin_foreigner_id = $model->id;
            }
            $detail->ministry_id = @$post['model']['ministry']['id'];
            $detail->remark = @$post['model']['remark'];
            $detail->award_id = @$post['model']['award']['id'];
            $detail->national_group = @$post['model']['national_group'];
            $detail->national_personal = @$post['model']['national_personal'];
            $detail->million_group = @$post['model']['million_group'];
            $detail->million_personal = @$post['model']['million_personal'];
            $detail->hornor_group = @$post['model']['hornor_group'];
            $detail->hornor_personal = @$post['model']['hornor_personal'];

            if (!$detail->save()) throw new Exception(json_encode($detail->errors));
            $transaction->commit();
        } catch (Exception $exception) {
            $transaction->rollBack();
            MyHelper::response(HttpCode::INTERNAL_SERVER_ERROR, $exception->getMessage());
            return;
        }
    }

